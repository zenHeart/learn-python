# 整形
print('2 + 2:', 2+2)
print('2 - 2:', 2-2)
print('2 * 2:', 2*2)
print('17 / 3:', 17/3)
print('17 // 3:', 17//3)  # 向下取整
print('17 % 3:', 17 % 3)  # 取模
print('2 ** 3:', 2**3)  # 幂运算

# 浮点
print('2.0 + 2:', 2.0+2)
print('2.0 - 2:', 2.0-2)
print('2 * 2.0:', 2*2.0)
print('17.0 / 3:', 17.0/3)
print('17.0 // 3:', 17.0//3)  # 向下取整
print('17.0 % 3:', 17.0 % 3)  # 取模
print('2 ** 3.1:', 2**3.1)  # 幂运算

# 虚数
print(' 2+3j+4j', 2+3j+4j)
print('2+3j*4j', 2+3j*4j)
